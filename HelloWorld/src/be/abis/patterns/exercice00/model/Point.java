package be.abis.patterns.exercice00.model;

public class Point {
    // list of attributes - x and y coordinates
    private double x;
    private double y;

    // default constructor
    public Point() {

    }

    // non-default constructor
    public Point(double x, double y) {

    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
